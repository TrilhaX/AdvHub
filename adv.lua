        local function UseActiveAttackE()
            local goat = game.Players.LocalPlayer
            local erwin1 = {}
        
            for _, v in pairs(game:GetService("Workspace")._UNITS:GetChildren()) do
                if v.Name == 'erwin' and v._stats.player.Value == goat then
                    table.insert(erwin1, v)
                end
            end
        
            local attacking = false
            local toggle = true
        
            while toggle do
                for i, erwin in ipairs(erwin1) do
                    if toggle and not attacking then
                        game:GetService("ReplicatedStorage"):WaitForChild("endpoints"):WaitForChild("client_to_server"):WaitForChild("use_active_attack"):InvokeServer(erwin)
                        attacking = true
                        wait(15.4)
                        attacking = false
                    end
                end
                wait() -- Add a wait statement to prevent the loop from running too frequently
            end
        end
        
